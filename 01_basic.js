// 01_数据类型

// Number： JavaScript不区分整数和浮点数，统一用Number表示，还可用十六进制(0x)表示，ES6后还支持二进制和八进制表示，以后都是合法Number类型：
123;    // 整数123
0.456;  // 浮点数0.456
1.2345e3;   // 科学计数法表示1.2345x1000，等同于1234.5
-99;    // 负数
NaN;    // NaN表示Not a Number，当无法计算结果时用NaN表示
Infinity;   // Infinity表示无限大，当数值超过了JavaScript的Number所能表示的最大值时，就表示为Infinity

// String: 用单引号(')或双引号(")括起来的任意文本
"abcde";
"123";

// 布尔值：只有true和false两种值
true;
false;
2 > 1;  // true

